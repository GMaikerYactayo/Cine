package modelo;

public class Sala {

    private int IDSAL;
    private String CAPSAL;
    private String ESTSAL;
    private String NUMSAL;

    public int getIDSAL() {
        return IDSAL;
    }

    public void setIDSAL(int IDSAL) {
        this.IDSAL = IDSAL;
    }

    public String getCAPSAL() {
        return CAPSAL;
    }

    public void setCAPSAL(String CAPSAL) {
        this.CAPSAL = CAPSAL;
    }

    public String getESTSAL() {
        return ESTSAL;
    }

    public void setESTSAL(String ESTSAL) {
        this.ESTSAL = ESTSAL;
    }

    public String getNUMSAL() {
        return NUMSAL;
    }

    public void setNUMSAL(String NUMSAL) {
        this.NUMSAL = NUMSAL;
    }

    
    
}
